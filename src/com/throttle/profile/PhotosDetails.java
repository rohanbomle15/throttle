package com.throttle.profile;

public class PhotosDetails {

	private String UserDocumentID = "";
	private String UserID = "";
	private String DocumentName = "";
	private String DocumentNumber = "";
	private String ExpiryDate = "";
	private String DocumentImageURL = "";
	public String getUserDocumentID() {
		return UserDocumentID;
	}
	public void setUserDocumentID(String userDocumentID) {
		UserDocumentID = userDocumentID;
	}
	public String getUserID() {
		return UserID;
	}
	public void setUserID(String userID) {
		UserID = userID;
	}
	public String getDocumentName() {
		return DocumentName;
	}
	public void setDocumentName(String documentName) {
		DocumentName = documentName;
	}
	public String getDocumentNumber() {
		return DocumentNumber;
	}
	public void setDocumentNumber(String documentNumber) {
		DocumentNumber = documentNumber;
	}
	public String getExpiryDate() {
		return ExpiryDate;
	}
	public void setExpiryDate(String expiryDate) {
		ExpiryDate = expiryDate;
	}
	public String getDocumentImageURL() {
		return DocumentImageURL;
	}
	public void setDocumentImageURL(String documentImageURL) {
		DocumentImageURL = documentImageURL;
	}
	
}
