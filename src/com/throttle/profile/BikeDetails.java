package com.throttle.profile;

public class BikeDetails {

	private String UserBikeID = "";
	private String UserID = "";
	private String BikeRegNo = "";
	private String BikeChassisNo = "";
	private String BikeMake = "";
	private String BikeModel = "";
	private String BikeImageURL = "";
	private String StatusCode = "";
	private String StatusDescription = "";
	public String getUserBikeID() {
		return UserBikeID;
	}
	public void setUserBikeID(String userBikeID) {
		UserBikeID = userBikeID;
	}
	public String getUserID() {
		return UserID;
	}
	public void setUserID(String userID) {
		UserID = userID;
	}
	public String getBikeRegNo() {
		return BikeRegNo;
	}
	public void setBikeRegNo(String bikeRegNo) {
		BikeRegNo = bikeRegNo;
	}
	public String getBikeChassisNo() {
		return BikeChassisNo;
	}
	public void setBikeChassisNo(String bikeChassisNo) {
		BikeChassisNo = bikeChassisNo;
	}
	public String getBikeMake() {
		return BikeMake;
	}
	public void setBikeMake(String bikeMake) {
		BikeMake = bikeMake;
	}
	public String getBikeModel() {
		return BikeModel;
	}
	public void setBikeModel(String bikeModel) {
		BikeModel = bikeModel;
	}
	public String getBikeImageURL() {
		return BikeImageURL;
	}
	public void setBikeImageURL(String bikeImageURL) {
		BikeImageURL = bikeImageURL;
	}
	public String getStatusCode() {
		return StatusCode;
	}
	public void setStatusCode(String statusCode) {
		StatusCode = statusCode;
	}
	public String getStatusDescription() {
		return StatusDescription;
	}
	public void setStatusDescription(String statusDescription) {
		StatusDescription = statusDescription;
	}
	
	
	
}
